        {

            DataSet ds = new DataSet();
            try
            {
                XSSFWorkbook workbook;
                IWorkbook wb;
                using (FileStream file = new FileStream(strFileName, FileMode.Open, FileAccess.Read))
                {
                    //wb = XSSFShapeFactory.Create(file);
                    wb = new XSSFWorkbook(file);
                }
                int index = 0;
                while (true)
