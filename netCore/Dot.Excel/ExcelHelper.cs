        /// <summary>
        /// DataTable导出到Excel文件
        /// </summary>
        /// <param name="dtSource">源DataTable</param>
        /// <param name="strHeaderText">表头文本</param>
        /// <param name="strFileName">保存位置</param>
        public static void ExportDTtoExcel(DataTable dtSource, string strHeaderText, string sheetName, string strFileName)
        {
            string[] temp = strFileName.Split('.');

            if (temp[temp.Length - 1] == "xls" && dtSource.Columns.Count < 256 && dtSource.Rows.Count < 65536)
            {
                using (MemoryStream ms = ExportDT(dtSource, strHeaderText, sheetName))
                {
                    using (FileStream fs = new FileStream(strFileName, FileMode.Create, FileAccess.Write))
                    {
                        byte[] data = ms.ToArray();
                        fs.Write(data, 0, data.Length);
                        fs.Flush();
                    }
                }
            }
            else
            {
                if (temp[temp.Length - 1] == "xls")
                    strFileName = strFileName + "x";

                using (FileStream fs = new FileStream(strFileName, FileMode.Create, FileAccess.Write))
                {
                    ExportDT(dtSource, strHeaderText, sheetName, fs);

                }

            }
        }


        public static bool ExportDStoExcel(DataSet ds, string excelFilename)
        {
            try
            {
                string[] temp = excelFilename.Split('.');
                bool useXSSF = false;
                foreach (DataTable item in ds.Tables)
                {
                    if (item.Columns.Count > 255 || item.Rows.Count > 65535)
                    {
                        useXSSF = true;
                        break;
                    }
                }

                using (FileStream SaveFile = new FileStream(excelFilename, FileMode.Create, FileAccess.ReadWrite))
                {
                    IWorkbook workbook;
                    if (temp[temp.Length - 1] == "xls" && !useXSSF)
                    {
                        workbook = new HSSFWorkbook();
                    }
                    else
                    {
                        workbook = new XSSFWorkbook();

                    }
                    WriteExcelFile(ds, workbook);
                    workbook.Write(SaveFile);
                }
                return true;
            }
            catch (System.Exception ex)
            {
                return false;
            }
        }

        private static void WriteExcelFile(DataSet ds, IWorkbook workbook)
        {
            foreach (DataTable dt in ds.Tables)
            {
                ISheet sheet = workbook.CreateSheet(dt.TableName);
                WriteDataTableToExcelWorksheet(dt, workbook, sheet);

            }
        }

        private static void WriteDataTableToExcelWorksheet(DataTable dt, IWorkbook workbook,ISheet sheet)
        {
            //取得列宽
            int[] arrColWidth = new int[dt.Columns.Count];
            foreach (DataColumn item in dt.Columns)
            {
                arrColWidth[item.Ordinal] = Encoding.UTF8.GetBytes(item.ColumnName.ToString()).Length;
            }
            for (int i = 0; i < dt.Rows.Count; i++)
            {
                for (int j = 0; j < dt.Columns.Count; j++)
                {
                    int intTemp = Encoding.UTF8.GetBytes(dt.Rows[i][j].ToString()).Length;
                    if (intTemp > arrColWidth[j])
                    {
                        arrColWidth[j] = intTemp;
                    }
                }
            }
            //生成sheet第一行列名 
            IRow row = sheet.CreateRow(0);
            for (int i = 0; i < dt.Columns.Count; i++)
            {
                ICell cell = row.CreateCell(i); 
                ICellStyle headStyle = workbook.CreateCellStyle() ;
                headStyle.Alignment = HorizontalAlignment.Center;
                IFont font = workbook.CreateFont() ;
                font.FontHeightInPoints = 10;
                font.Boldweight = 700;
                headStyle.SetFont(font);
                cell.CellStyle = headStyle;
                cell.SetCellValue(dt.Columns[i].Caption);
                sheet.SetColumnWidth(i, (arrColWidth[i] + 1) * 256);
            }

            //写入数据
            for (int i = 0; i < dt.Rows.Count; i++)
            {
                IRow rows = sheet.CreateRow(i + 1);
                for (int j = 0; j < dt.Columns.Count; j++)
                {
                    var cellValue = dt.Rows[i][j];
                    if (cellValue == DBNull.Value)
                    {
                        continue;
                    }
                    if (dt.Columns[j].DataType == typeof(string))
                    {
                        ICell cell = rows.CreateCell(j);
                        cell.SetCellValue(cellValue.ToString());
                    }
                    else if (dt.Columns[j].DataType == typeof(double))
                    {
                        ICell cell = rows.CreateCell(j, Npoi.Core.SS.UserModel.CellType.Numeric);
                        double cellNumericValue;
                        if (double.TryParse(cellValue.ToString(), out cellNumericValue))
                        {
                            cell.SetCellValue(cellNumericValue);
                        }
                    }

                    ////暂时只判断数值和字符类型
                    //double cellNumericValue;

                    //if (double.TryParse(cellValue.ToString(), out cellNumericValue))
                    //{
                    //    cell.SetCellValue(cellNumericValue);
                    //}
                    //else
                    //{
                    //    cell.SetCellValue(cellValue.ToString());
                    //}
                }
            }
        }



